export function buildDailyReportPrompt({
  input,
  fallbackReport,
}: {
  input: DailyHealthReportInput;
  fallbackReport: DailyHealthReport;
}) {
  return [
    '[입력 데이터]',
    'DailyHealthReportInput:',
    JSON.stringify(input, null, 2),
    '',
    '[기준 리포트]',
    'Fallback DailyHealthReport:',
    JSON.stringify(fallbackReport, null, 2),
    '',
    '[작성 지시]',
    '- Fallback DailyHealthReport와 같은 JSON 구조로 응답하세요.',
    '- date, nutrition, steps는 Fallback DailyHealthReport의 값을 그대로 사용하세요.',
    '- summary와 insights만 더 자연스럽고 도움이 되는 표현으로 작성하세요.',
    '- 최종 응답은 순수 JSON 객체 한 개만 반환하세요.',
  ].join('\n');
}
